texto = 'Hola, bienvenido al curso profesional de python en codigofacilito'

# Usamos el metodo startswith() para buscar una palabra en una cadena
# El texto no inicia con la palabra 'bienvenido'
print(texto.startswith('bienvenido'))

# Podemos saber si la cadena de texto termina con una palabra
# El texto no termina con la palabra 'final'
print(texto.endswith('final'))